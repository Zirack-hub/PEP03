# Para poner un caracter unicode hay que poner "\u" (para la notación simple) o "\U" (para la notación extendida) o "\x" (para la notación hexadecimal) y los numeros referentes al caracter
print('Hola\u0020Mundo')
print('Notación simple:','\u0041')
print('Notación extendida:','\U00000041')
print('Notación hexadecimal;','\x41')
print('Corazón:','\u2665')
print('Cara sonriendo:','\U0001f600')
print('Serpinete:','\U0001f40d')