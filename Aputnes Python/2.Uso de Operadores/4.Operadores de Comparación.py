a = 4
b = 2
# El simbolo == se utiliza para saber si dos valores son iguales
resultado = (a == b)
print(f'Resultado == : {resultado}')
# El simbolo != se utiliza para saber si dos valores son diferentes
resultado = (a != b)
print(f'Resultado != : {resultado}')
# El simbolo > se utiliza para saber si un valor es mayor que otro
resultado = (a > b)
print(f'Resultado > : {resultado}')
# El simbolo >= se utiliza para saber si un valor es mayor o igual que otro
resultado = (a >= b)
print(f'Resultado >= : {resultado}')
# El simbolo < se utiliza para saber si un valor es menor que otro
resultado = (a < b)
print(f'Resultado < : {resultado}')
# El simbolo <= se utiliza para saber si un valor es menor o igual que otro
resultado = (a <= b)
print(f'Resultado <= : {resultado}')