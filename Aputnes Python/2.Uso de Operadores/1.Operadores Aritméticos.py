operandoA = 3
operandoB = 2
# Suma con dos sintaxis distintas
suma = operandoA + operandoB
print('Resultado de la suma:', suma)
print(f'Resultado de la suma: {suma}')
# Resta
resta = operandoA - operandoB
print(f'Resultado de la resta: {resta}')
# Multiplicación
multiplicacion = operandoA * operandoB
print(f'Resultado de la multiplicación: {multiplicacion}')
# División en numeros flotantes
division = operandoA / operandoB
print(f'Resultado de la división: {division}')
# División en numeros enteros
division = operandoA // operandoB
print(f'Resultado de la división (int): {division}')
# Resto de la division entre dos numeros
modulo = operandoA % operandoB
print(f'Resultado del modulo o residuo de la división: {modulo}')
# Exponente: un numero elevado al otro
exponente = operandoA ** operandoB
print(f'Resultado del exponente de la división: {exponente}')