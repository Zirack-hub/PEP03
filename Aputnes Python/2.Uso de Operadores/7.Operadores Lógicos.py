a = True
b = False
# El operador "and" da True siempre que las dos variables sean True en caso contrario dara False
resultado = a and b
print(f'El resultado de and es: {resultado}')
# El operador "or" da True siempre que una de las dos variables sea True en caso contrario dara False
resultado = a or b
print(f'El resultado de or es: {resultado}')
# El operador "not" invierte el valor de la variable siendo True = False o False = True
resultado = not a
print(f'El resultado de not es: {resultado}')
resultado = not b
print(f'El resultado de not es: {resultado}')