# El tipo de dato de a es "int"
a = 10
print(a)
print(type(a))
# El tipo de dato de b es "float"
b = 10.5
print(b)
print(type(b))
# El tipo de dato de c es "string"
c = "Hola Mundo"
print(c)
print(type(c))
# El tipo de dato de d es "bool"
d = True
print(d)
print(type(d))
d = False
print(d)
print(type(d))