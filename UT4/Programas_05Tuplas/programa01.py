tupla_int = (1, 2, 3)
tupla_float = (3.14, 2.71, 0.001)
tupla_str = ("hola", "mundo")
tupla_bool = (True, False, True)

# Tupla vacía
tupla_vacia = ()

# Tupla con un solo elemento (IMPORTANTE: lleva coma)
tupla_un_elemento = (42,)

# Tupla sin paréntesis (tuple packing)
tupla_sin_parentesis = 10, 20, "python", 3.5

# Mostrar tipos
print("tupla_int:", type(tupla_int))
print("tupla_float:", type(tupla_float))
print("tupla_str:", type(tupla_str))
print("tupla_bool:", type(tupla_bool))
print("tupla_vacia:", type(tupla_vacia))
print("tupla_un_elemento:", type(tupla_un_elemento))
print("tupla_sin_parentesis:", type(tupla_sin_parentesis))