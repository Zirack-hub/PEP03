#Crea un conjunto con diferentes números.
#Muestra su longitud con len().
#Comprueba si ciertos valores están o no en el conjunto usando in y not in.
#Indica cuántos elementos tiene la tupla con len().
conjunto_numeros = {10, 20, 30, 40, 50}
print("Longitud del conjunto:", len(conjunto_numeros))
print("¿El 20 está en el conjunto?", 20 in conjunto_numeros)
print("¿El 60 no está en el conjunto?", 20 not in conjunto_numeros)
tupla_numeros = (10, 20, 30, 40, 50)
print("Longitud de la tupla:", len(tupla_numeros))